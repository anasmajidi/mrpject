    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String name = c_name.getText();
        String tp = c_tp.getText();
        
        try {
            Statement s = db.mycon().createStatement();
            s.executeUpdate("INSERT INTO customer (customer,to_Number)VALUES  ('"+name+"','"+tp+"')");
            JOptionPane.showMessageDialog(null, "Data saved");
            
        } catch (SQLException e) {
            System.out.println(e);
        }
        tb_load();
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Search :
        String search = c_search.getText();
        try {
            
            Statement s = db.mycon().createStatement();
            
            ResultSet rs = s.executeQuery(" SELECT * FROM customer WHERE cid = '"+search+"'");
            
            if(rs.next()){
                c_name.setText(rs.getString("customer"));
                c_tp.setText(rs.getString("to_Number"));
            }
            
        } catch (SQLException e){
            System.out.println(e);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // UPDATE
        
        String name = c_name.getText();
        String tp = c_tp.getText();
        String id = c_search.getText();
        try {
            
            Statement s = db.mycon().createStatement();
            s.executeUpdate(" UPDATE customer SET customer ='"+name+"' ,to_Number ='"+tp+"' WHERE cid = '"+id+"'");
            JOptionPane.showMessageDialog(null, "Data Updated");
        } catch(Exception e){
            System.out.println(e);
        }
        tb_load();
        
    }//GEN-LAST:event_jButton3ActionPerformed

